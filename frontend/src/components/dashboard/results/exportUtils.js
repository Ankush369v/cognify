import jsPDF from 'jspdf';

export const handleExportPDF = (content, setIsExporting) => {
    setIsExporting(true);
    try {
        const doc = new jsPDF();
        const pageWidth = doc.internal.pageSize.getWidth();
        const pageHeight = doc.internal.pageSize.getHeight();
        const margin = 20;
        const contentWidth = pageWidth - (margin * 2);
        let yPos = 20;

        const checkPageBreak = (heightNeeded) => {
            if (yPos + heightNeeded >= pageHeight - margin) {
                doc.addPage();
                yPos = margin;
            }
        };

        const addWrappedText = (text, fontSize = 12, fontStyle = 'normal', color = [0, 0, 0], xOffset = 0) => {
            if (!text) return;
            doc.setFontSize(fontSize);
            doc.setFont("helvetica", fontStyle);
            doc.setTextColor(...color);

            const lines = doc.splitTextToSize(String(text), contentWidth - (xOffset * 2));
            const lineHeight = fontSize * 0.5;

            checkPageBreak(lines.length * lineHeight + 5);

            doc.text(lines, margin + xOffset, yPos);
            yPos += (lines.length * lineHeight) + 4;
        };

        const addSeparator = () => {
            checkPageBreak(10);
            doc.setDrawColor(200, 200, 200);
            doc.line(margin, yPos, pageWidth - margin, yPos);
            yPos += 10;
        };

        // Header
        doc.setFontSize(22);
        doc.setTextColor(0, 0, 0);
        doc.text(`${content.type.toUpperCase().replace('-', ' ')} RESULT`, margin, yPos);
        yPos += 10;

        doc.setFontSize(10);
        doc.setTextColor(100, 100, 100);
        doc.text(`Generated by: ${content.meta?.model || 'AI Model'}`, margin, yPos);
        yPos += 6;
        doc.text(`Date: ${new Date(content.createdAt).toLocaleDateString()}`, margin, yPos);
        yPos += 15;

        addSeparator();

        // 1. MCQ
        if (content.type === 'mcq') {
            const mcqs = Array.isArray(content.data) ? content.data : [];
            mcqs.forEach((q, idx) => {
                checkPageBreak(40);
                addWrappedText(`Q${idx + 1}: ${q.question}`, 14, 'bold');
                if (Array.isArray(q.options)) {
                    q.options.forEach((opt, i) => {
                        const letter = String.fromCharCode(65 + i);
                        const isCorrect = letter === q.correct;
                        const color = isCorrect ? [0, 150, 0] : [80, 80, 80];
                        addWrappedText(`${letter}) ${opt}`, 12, isCorrect ? 'bold' : 'normal', color);
                    });
                } else if (q.answer) {
                    addWrappedText(`Answer: ${q.answer}`, 12, 'italic', [0, 100, 0]);
                }
                if (q.explanation) {
                    yPos += 2;
                    addWrappedText(`Explanation: ${q.explanation}`, 10, 'italic', [100, 100, 100]);
                }
                yPos += 8;
            });
        }
        // 2. FLASHCARDS
        else if (content.type === 'flashcards') {
            const cards = Array.isArray(content.data) ? content.data : [];
            cards.forEach((card) => {
                const fontSize = 12;
                const lineHeight = fontSize * 0.5;
                const internalPaddingX = 8;
                const internalPaddingY = 10;
                
                // Calculate actual lines to determine height
                const frontLines = doc.splitTextToSize(`Front: ${card.question}`, contentWidth - (internalPaddingX * 2));
                const backLines = doc.splitTextToSize(`Back: ${card.answer}`, contentWidth - (internalPaddingX * 2));
                
                const cardHeight = (frontLines.length + backLines.length) * lineHeight + (internalPaddingY * 2) + 4; 
                
                checkPageBreak(cardHeight + 10);
                
                const startY = yPos;
                doc.setFillColor(245, 247, 250);
                doc.roundedRect(margin, startY, contentWidth, cardHeight, 3, 3, 'F');
                
                yPos += internalPaddingY;
                addWrappedText(`Front: ${card.question}`, 12, 'bold', [0, 0, 0], internalPaddingX);
                yPos -= 2; // small adjustment between front and back
                addWrappedText(`Back: ${card.answer}`, 12, 'normal', [0, 0, 150], internalPaddingX);
                
                yPos = startY + cardHeight + 8; // Margin after card
            });
        }
        // 3. SUMMARY
        else if (content.type === 'summary') {
            const text = typeof content.data === 'string' ? content.data : JSON.stringify(content.data);
            const lines = text.split('\n');
            
            lines.forEach(line => {
                const trimmed = line.trim();
                if (!trimmed) {
                    yPos += 4;
                    return;
                }

                let xOffset = 0;
                let currentLine = line;

                // Handle Bullets
                if (trimmed.startsWith('*') || trimmed.startsWith('-') || trimmed.startsWith('+')) {
                    doc.setFontSize(14);
                    doc.setTextColor(79, 70, 229);
                    doc.text("•", margin, yPos + 1);
                    xOffset = 6;
                    currentLine = line.replace(/^\s*[\*\-\+]\s*/, '');
                }

                // Simple Inline Bold Parser (Strip stars and use Bold for whole line if it contains them)
                // For a more complex inline bolding, we'd need word-by-word positioning.
                // We'll prioritize readability by removing stars and using bold for header-like lines.
                const hasBold = currentLine.includes('**');
                const cleanLine = currentLine.replace(/\*\*/g, '');
                
                const isHeader = hasBold && (cleanLine.length < 100 || trimmed.startsWith('*'));
                
                doc.setFontSize(12);
                doc.setTextColor(content.type === 'summary' ? 40 : 100, 40, 40);
                doc.setFont("helvetica", isHeader ? "bold" : "normal");

                const wrappedLines = doc.splitTextToSize(cleanLine, contentWidth - xOffset);
                const lineHeight = 6;
                
                checkPageBreak(wrappedLines.length * lineHeight + 5);
                doc.text(wrappedLines, margin + xOffset, yPos);
                yPos += (wrappedLines.length * lineHeight) + 2;
            });
        }
        // 4. CODE TOOLS
        else if (content.type === 'explain' || content.type === 'review' || content.type === 'code-tools') {
            const data = content.data || {};
            const clean = (str) => String(str || '').replace(/\*\*/g, '').replace(/`/g, '');

            // Summary & Complexity
            addWrappedText("EXECUTIVE SUMMARY", 16, 'bold', [0, 0, 0]);
            addWrappedText(clean(data.summary || data.review_summary), 11);
            yPos += 5;

            if (data.complexity) {
                doc.setFillColor(240, 244, 255);
                doc.setDrawColor(79, 70, 229);
                doc.roundedRect(margin, yPos, contentWidth, 18, 2, 2, 'FD');
                const startY = yPos;
                yPos += 7;
                addWrappedText(`Complexity Analysis: ${clean(data.complexity)}`, 11, 'bold', [79, 70, 229], 5);
                yPos = startY + 22;
            }

            // Key Learnings
            if (data.key_learnings?.length) {
                addWrappedText("ARCHITECTURAL INSIGHTS", 14, 'bold', [0, 0, 0]);
                data.key_learnings.forEach(k => {
                    addWrappedText(`• ${clean(k)}`, 11, 'normal', [60, 60, 60], 5);
                });
                yPos += 5;
            }

            // Functions
            if (Array.isArray(data.functions)) {
                data.functions.forEach(fn => {
                    addSeparator();
                    addWrappedText(`Module: ${clean(fn.name)}`, 14, 'bold', [0, 50, 150]);
                    
                    if (fn.description) {
                        addWrappedText("Description:", 11, 'bold');
                        addWrappedText(clean(fn.description), 11);
                    }
                    
                    if (fn.logic) {
                        yPos += 2;
                        addWrappedText("Logic Breakdown:", 11, 'bold');
                        addWrappedText(clean(fn.logic), 11);
                    }

                    if (fn.correctness) {
                        yPos += 2;
                        addWrappedText("Correctness Audit:", 10, 'bold', [0, 120, 0]);
                        addWrappedText(clean(fn.correctness), 10);
                    }

                    if (fn.potentialIssues?.length) {
                        yPos += 3;
                        addWrappedText("Potential Issues:", 11, 'bold', [180, 80, 0]);
                        fn.potentialIssues.forEach(pi => addWrappedText(`- ${clean(pi)}`, 10, 'normal', [100, 50, 0], 5));
                    }

                    if (fn.optimizations?.length) {
                        yPos += 3;
                        addWrappedText("Suggested Optimizations:", 11, 'bold', [0, 100, 180]);
                        fn.optimizations.forEach(op => addWrappedText(`→ ${clean(op)}`, 10, 'normal', [0, 80, 150], 5));
                    }
                    yPos += 5;
                });
            }
        }
        // 5. DATA ANALYSIS & VISUALIZATION
        else if (content.type === 'analysis' || content.type === 'visualize') {
            const data = content.data || {};
            const clean = (val) => {
                if (!val) return '';
                if (typeof val === 'object') {
                    return String(val.insight || val.text || val.value || val.message || JSON.stringify(val)).replace(/\*\*/g, '').replace(/`/g, '');
                }
                return String(val).replace(/\*\*/g, '').replace(/`/g, '');
            };
            
            const mainTitle = data.dashboard_title || (content.type === 'visualize' ? "Visual Intelligence Dashboard" : "Advanced Data Analysis Report");
            addWrappedText(mainTitle, 18, 'bold', [79, 70, 229]);

            const summaryText = data.executive_summary || data.scentific_summary;
            if (summaryText) {
                addWrappedText("EXECUTIVE OVERVIEW", 12, 'bold');
                addWrappedText(clean(summaryText), 11);
                yPos += 5;
            }

            // KPIs in a simple grid-like layout
            if (data.kpis?.length) {
                addWrappedText("KEY PERFORMANCE METRICS", 12, 'bold', [79, 70, 229]);
                data.kpis.forEach(k => {
                    addWrappedText(`${clean(k.label)}: ${clean(k.value)} (${k.trend || 'stable'})`, 11, 'normal', [40, 40, 40], 5);
                });
                yPos += 5;
            }

            if (data.data_quality_audit) {
                addSeparator();
                const score = data.data_quality_audit.score || 'N/A';
                addWrappedText(`DATA QUALITY AUDIT (Score: ${score}/100)`, 13, 'bold', [0, 80, 180]);
                addWrappedText(clean(data.data_quality_audit.details), 11, 'italic', [80, 80, 80]);
                yPos += 5;
            }

            if (data.statistical_insights?.length) {
                addSeparator();
                addWrappedText("STATISTICAL DEEP DIVE", 13, 'bold', [75, 0, 130]);
                data.statistical_insights.forEach(insight => {
                    addWrappedText(`• ${clean(insight)}`, 11, 'normal', [50, 50, 50], 5);
                });
                yPos += 5;
            }

            if (data.market_trends) {
                addSeparator();
                addWrappedText("MARKET TRENDS & PATTERNS", 13, 'bold', [0, 128, 128]);
                if (data.market_trends.overview) {
                    addWrappedText(clean(data.market_trends.overview), 11, 'italic');
                }
                if (data.market_trends.details?.length) {
                    data.market_trends.details.forEach(t => addWrappedText(`• ${clean(t)}`, 11, 'normal', [50, 50, 50], 5));
                }
                yPos += 5;
            }

            if (data.segmentation_analysis) {
                addWrappedText("SEGMENTATION & GROUPING", 12, 'bold', [150, 0, 150]);
                addWrappedText(clean(data.segmentation_analysis), 11);
                yPos += 5;
            }

            if (data.anomalies?.length) {
                addSeparator();
                addWrappedText("ANOMALIES & OUTLIERS DETECTED", 13, 'bold', [200, 100, 0]);
                data.anomalies.forEach(a => addWrappedText(`! ${clean(a)}`, 11, 'normal', [180, 50, 0], 5));
                yPos += 5;
            }

            if (data.recommendations?.length) {
                addSeparator();
                addWrappedText("STRATEGIC RECOMMENDATIONS", 14, 'bold', [0, 120, 0]);
                data.recommendations.forEach(r => addWrappedText(`→ ${clean(r)}`, 11, 'bold', [0, 80, 0], 5));
                yPos += 5;
            }
        }

        doc.save(`${content.type}-report.pdf`);
    } catch (error) {
        console.error("Export failed", error);
        alert("Failed to export PDF.");
    } finally {
        setIsExporting(false);
    }
};
